/*
Name: updateMusic

Purpose: updates the current note that is playing during the main game loop

Input: music - the structure containing all music data (channel B activated and currentNote), type Music*

Details: Once the music reaches the last note it will then restart at the 
*/
void updateMusic(struct Music* music)
{
	if(musicA[music->currentNote] > 1)
	{
		set_tone(0, musicA[music->currentNote]);
		set_volume(0, 10);
	}
	else if(musicA[music->currentNote] == 0)
	{
		set_volume(0, 0);
	}
	if(music->bChannelActivated)
	{
		if(musicB[music->currentNote] > 1)
		{
			set_tone(1, musicB[music->currentNote]);
			set_volume(1, 10);
		}
		else if(musicB[music->currentNote] == 0)
		{
			set_volume(1, 0);
		}
	}
	
	if(music->currentNote == END_MUSIC)
	{
		music->currentNote = START_MUSIC_LOOP;
	}
	else
	{
		if(music->currentNote == MUSIC_B_ON_1 || music->currentNote == MUSIC_B_ON_2 || music->currentNote == MUSIC_B_ON_3)
		{
			music->bChannelActivated = TRUE;
		}
		else if(music->currentNote == START_MUSIC_LOOP || music->currentNote == MUSIC_B_OFF_1)
		{
			music->bChannelActivated = FALSE;
		}
		music->currentNote++;
	}
	
	return;
}





